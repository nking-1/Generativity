import Prelude hiding (fst, snd, div)

data Prod a b =
   Pair a b

fst :: (Prod a1 a2) -> a1
fst p =
  case p of {
   Pair x _ -> x}

snd :: (Prod a1 a2) -> a2
snd p =
  case p of {
   Pair _ y -> y}

add :: Integer -> Integer -> Integer
add n m =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> m)
    (\p -> (\n -> n + 1) (add p m))
    n

mul :: Integer -> Integer -> Integer
mul n m =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> 0)
    (\p -> add m (mul p m))
    n

sub :: Integer -> Integer -> Integer
sub n m =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> n)
    (\k ->
    (\fO fS n -> if n == 0 then fO () else fS (n - 1))
      (\_ -> n)
      (\l -> sub k l)
      m)
    n

sub0 :: Integer -> Integer -> Integer
sub0 n m =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> n)
    (\k ->
    (\fO fS n -> if n == 0 then fO () else fS (n - 1))
      (\_ -> n)
      (\l -> sub0 k l)
      m)
    n

divmod :: Integer -> Integer -> Integer -> Integer -> Prod Integer Integer
divmod x y q u =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> Pair q u)
    (\x' ->
    (\fO fS n -> if n == 0 then fO () else fS (n - 1))
      (\_ -> divmod x' y ((\n -> n + 1) q) y)
      (\u' -> divmod x' y q u')
      u)
    x

div :: Integer -> Integer -> Integer
div x y =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> y)
    (\y' -> fst (divmod x y' 0 y'))
    y

modulo :: Integer -> Integer -> Integer
modulo x y =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> x)
    (\y' -> sub0 y' (snd (divmod x y' 0 y')))
    y

