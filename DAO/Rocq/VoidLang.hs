leb :: Prelude.Integer -> Prelude.Integer -> Prelude.Bool
leb n m =
  (\fO fS n -> if n Prelude.== 0 then fO () else fS (n Prelude.- 1))
    (\_ -> Prelude.True)
    (\n' ->
    (\fO fS n -> if n Prelude.== 0 then fO () else fS (n Prelude.- 1))
      (\_ -> Prelude.False)
      (\m' -> leb n' m')
      m)
    n

divmod :: Prelude.Integer -> Prelude.Integer -> Prelude.Integer ->
          Prelude.Integer -> Prod Prelude.Integer Prelude.Integer
divmod x y q u =
  (\fO fS n -> if n Prelude.== 0 then fO () else fS (n Prelude.- 1))
    (\_ -> Pair q u)
    (\x' ->
    (\fO fS n -> if n Prelude.== 0 then fO () else fS (n Prelude.- 1))
      (\_ -> divmod x' y ((\n -> n Prelude.+ 1) q) y)
      (\u' -> divmod x' y q u')
      u)
    x

div :: Prelude.Integer -> Prelude.Integer -> Prelude.Integer
div = (\n m -> n `Prelude.div` m)

modulo :: Prelude.Integer -> Prelude.Integer -> Prelude.Integer
modulo = (\n m -> n `Prelude.mod` m)

eqb :: Prelude.String -> Prelude.String -> Prelude.Bool
eqb = (Prelude.==)

data Expr =
   ENum Prelude.Integer
 | EVoid
 | EBool Prelude.Bool
 | EAdd Expr Expr
 | ESub Expr Expr
 | EMul Expr Expr
 | EDiv Expr Expr
 | EMod Expr Expr
 | EIsVoid Expr
 | EIfVoid Expr Expr Expr
 | EDefault Expr Expr

data Value =
   VNum Prelude.Integer
 | VBool Prelude.Bool
 | VVoid

eval :: Prelude.Integer -> Expr -> Value
eval fuel e =
  (\fO fS n -> if n Prelude.== 0 then fO () else fS (n Prelude.- 1))
    (\_ -> VVoid)
    (\fuel' ->
    case e of {
     ENum n -> VNum n;
     EVoid -> VVoid;
     EBool b -> VBool b;
     EAdd e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 -> VNum (add n1 n2);
         _ -> VVoid};
       _ -> VVoid};
     ESub e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 -> VNum (sub n1 n2);
         _ -> VVoid};
       _ -> VVoid};
     EMul e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 -> VNum (mul n1 n2);
         _ -> VVoid};
       _ -> VVoid};
     EDiv e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 ->
          (\fO fS n -> if n Prelude.== 0 then fO () else fS (n Prelude.- 1))
            (\_ -> VVoid)
            (\_ -> VNum (div n1 n2))
            n2;
         _ -> VVoid};
       _ -> VVoid};
     EMod e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 ->
          (\fO fS n -> if n Prelude.== 0 then fO () else fS (n Prelude.- 1))
            (\_ -> VVoid)
            (\_ -> VNum (modulo n1 n2))
            n2;
         _ -> VVoid};
       _ -> VVoid};
     EIsVoid e0 ->
      case eval fuel' e0 of {
       VVoid -> VBool Prelude.True;
       _ -> VBool Prelude.False};
     EIfVoid cond then_branch else_branch ->
      case eval fuel' cond of {
       VVoid -> eval fuel' then_branch;
       _ -> eval fuel' else_branch};
     EDefault e0 default0 ->
      case eval fuel' e0 of {
       VVoid -> eval fuel' default0;
       x -> x}})
    fuel

