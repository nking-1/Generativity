data VoidInfo =
   VInfo Prelude.Integer Prelude.Integer VoidSource
data VoidSource =
   DivByZero Prelude.Integer
 | ModByZero Prelude.Integer
 | UndefinedVar Prelude.String
 | OutOfFuel
 | TypeError Prelude.String
 | VoidPropagation VoidInfo VoidInfo

data ValueT =
   VTNum Prelude.Integer
 | VTBool Prelude.Bool
 | VTVoid VoidInfo

data Universe =
   Build_Universe Prelude.Integer Prelude.Integer Prelude.Integer

total_entropy :: Universe -> Prelude.Integer
total_entropy u =
  case u of {
   Build_Universe total_entropy0 _ _ -> total_entropy0}

time_step :: Universe -> Prelude.Integer
time_step u =
  case u of {
   Build_Universe _ time_step0 _ -> time_step0}

void_count :: Universe -> Prelude.Integer
void_count u =
  case u of {
   Build_Universe _ _ void_count0 -> void_count0}

initial_universe :: Universe
initial_universe =
  Build_Universe 0 0 0

evolve_universe :: Universe -> VoidInfo -> Universe
evolve_universe u info =
  case info of {
   VInfo entropy _ _ -> Build_Universe (add (total_entropy u) entropy)
    ((\n -> n Prelude.+ 1) (time_step u)) ((\n -> n Prelude.+ 1)
    (void_count u))}

combine_voids :: VoidInfo -> VoidInfo -> Universe -> VoidInfo
combine_voids v1 v2 u =
  case v1 of {
   VInfo e1 _ _ ->
    case v2 of {
     VInfo e2 _ _ -> VInfo (add e1 e2) (time_step u) (VoidPropagation v1 v2)}}

