eval :: Integer -> Expr -> Value
eval fuel e =
  (\fO fS n -> if n == 0 then fO () else fS (n - 1))
    (\_ -> VVoid)
    (\fuel' ->
    case e of {
     ENum n -> VNum n;
     EVoid -> VVoid;
     EBool b -> VBool b;
     EAdd e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 -> VNum (add n1 n2);
         _ -> VVoid};
       _ -> VVoid};
     ESub e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 -> VNum (sub n1 n2);
         _ -> VVoid};
       _ -> VVoid};
     EMul e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 -> VNum (mul n1 n2);
         _ -> VVoid};
       _ -> VVoid};
     EDiv e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 ->
          (\fO fS n -> if n == 0 then fO () else fS (n - 1))
            (\_ -> VVoid)
            (\_ -> VNum (div n1 n2))
            n2;
         _ -> VVoid};
       _ -> VVoid};
     EMod e1 e2 ->
      case eval fuel' e1 of {
       VNum n1 ->
        case eval fuel' e2 of {
         VNum n2 ->
          (\fO fS n -> if n == 0 then fO () else fS (n - 1))
            (\_ -> VVoid)
            (\_ -> VNum (modulo n1 n2))
            n2;
         _ -> VVoid};
       _ -> VVoid};
     EIsVoid e0 ->
      case eval fuel' e0 of {
       VVoid -> VBool True;
       _ -> VBool False};
     EIfVoid cond then_branch else_branch ->
      case eval fuel' cond of {
       VVoid -> eval fuel' then_branch;
       _ -> eval fuel' else_branch};
     EDefault e0 default0 ->
      case eval fuel' e0 of {
       VVoid -> eval fuel' default0;
       x -> x}})
    fuel

